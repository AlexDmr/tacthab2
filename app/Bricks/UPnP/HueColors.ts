const A = str.split("\n").map( L => L.split("\t") ).map( ([name, hexa, rgb, hsl]) => ({name, hsl}) );

export type HSLColor = {
    bri: number; // 0-100
    hue: number; // 0-365
    sat: number; // 0-100
};

export const mapHueColors = new Map<string, HSLColor>();
A.forEach( ({name, hsl}) => {
    // If there is a / in the name...
    const keys = name.split("/").map( str => str.trim() );

    keys.forEach( k => mapHueColors.set(k, stringToHSL(hsl) ) );
});

export function stringToHSL(hsl: string): HSLColor {
    const ReColorHSL = new RegExp("^hsl\((\d*), *(\d*)%, *(\d*)%\)$" );
    const Res = ReColorHSL.exec(hsl);
    if (Res) {
        const hue = Res[1];
        const sat = Res[2];
        const bri = Res[3];
        return {
            hue: parseInt(hue),
            sat: parseInt(sat),
            bri: parseInt(bri)
        };
    } else {
        return undefined;
    }
}

export function getHSLColor(hsl: string) {
    return mapHueColors.get(hsl) || stringToHSL(hsl);
}
